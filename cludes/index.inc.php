<h2>SHA Breaker - Par Xylitol</h2>
<p>&nbsp;</p>
<p>SHA-Breaker est un projet visant le cassage des hashs SHA<br />
en utilisant une base de donn&eacute;es online.</p>
<p>&nbsp;</p>
<p>Voici &agrave; peu pr&egrave;s comment cela fonctionne:<br />
le visiteur entre le hash a d&eacute;crypter<br />
-&gt; recherche dans la base de donn&eacute;es si le hash et son equivalent texte existe</p>
<p>&nbsp;</p>
<p> -&gt; Si oui, afficher l'&eacute;quivalent<br />
  -&gt;
Si non afficher le message d'erreur</p>
